# STANDARD DNA CODON TABLE
# --------------------------
CODON_TABLE = {
    "TTT":"F","TTC":"F","TTA":"L","TTG":"L","CTT":"L","CTC":"L","CTA":"L","CTG":"L",
    "ATT":"I","ATC":"I","ATA":"I",
    "ATG":"M",
    "GTT":"V","GTC":"V","GTA":"V","GTG":"V",
    "TCT":"S","TCC":"S","TCA":"S","TCG":"S","AGT":"S","AGC":"S",
    "CCT":"P","CCC":"P","CCA":"P","CCG":"P",
    "ACT":"T","ACC":"T","ACA":"T","ACG":"T",
    "GCT":"A","GCC":"A","GCA":"A","GCG":"A",
    "TAT":"Y","TAC":"Y",
    "CAT":"H","CAC":"H",
    "CAA":"Q","CAG":"Q",
    "AAT":"N","AAC":"N",
    "AAA":"K","AAG":"K",
    "GAT":"D","GAC":"D",
    "GAA":"E","GAG":"E",
    "TGT":"C","TGC":"C",
    "TGG":"W",
    "CGT":"R","CGC":"R","CGA":"R","CGG":"R","AGA":"R","AGG":"R",
    "GGT":"G","GGC":"G","GGA":"G","GGG":"G",
    "TAA":"*","TAG":"*","TGA":"*"
}

# ============================================================
# FUNCTION 1: TRANSLATE DNA → PROTEIN
# ============================================================
def translate_dna_to_protein(dna_seq, frame=0, stop_at_stop=True):
    seq = dna_seq.upper().replace(" ", "").replace("\n", "")
    protein = []

    for i in range(frame, len(seq) - 2, 3):
        codon = seq[i:i+3]
        aa = CODON_TABLE.get(codon, "X")  # Unknown codons become 'X'

        if aa == "*":  # Stop codon
            if stop_at_stop:
                break
            protein.append("*")
        else:
            protein.append(aa)

    return "".join(protein)

# ============================================================
# FUNCTION 2: HAMMING DISTANCE
# ============================================================
def calculate_hamming_distance(str1, str2, pad_char="_"):
    max_len = max(len(str1), len(str2))
    str1 = str1.ljust(max_len, pad_char)
    str2 = str2.ljust(max_len, pad_char)

    return sum(c1 != c2 for c1, c2 in zip(str1, str2))

# ============================================================
# BASIC UNIT TESTS
