def test_translation():
    assert translate_dna_to_protein("ATGAAA") == "MK"
    assert translate_dna_to_protein("ATGTAATTT") == "M"
    assert translate_dna_to_protein("ATGNNA") == "MX"
    print("DNA Translation Tests Passed ✓")

def test_hamming():
    assert calculate_hamming_distance("abc", "abc") == 0
    assert calculate_hamming_distance("abc", "axc") == 1
    assert calculate_hamming_distance("abc", "abcd") == 1
    assert calculate_hamming_distance("", "") == 0
    print("Hamming Distance Tests Passed ✓")

test_translation()
test_hamming()

